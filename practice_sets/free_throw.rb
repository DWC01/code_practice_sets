Free Throw Question

Q.) Suppose you are shooting free throws and each shot has a 60% chance of going in (there is no "learning" effect and "depreciation" effect, all have the some probability no matter how many shots you take).

Now there are three scenarios where you can win $1000

Make at least 2 out of 3
Make at least 4 out of 6
Make at least 20 out of 30

A.) For this question, you have the best chance of making 20 / 30, since you are a 60% free throw shooter. If by bad luck 'chance' you miss 2 of the first 3, it would not affect your overall score as drastically. 


GENERAL ANSWER 

It depends how good you are. 

- If you are good and your percentage is 60%, you would want more shots because on average, you would have a better chance of shooting over 60%.

- If your average was below 60%, than you would want to shoot as little as posisble, this would minimize your bad shooting ability and allow you get lucky ftw. 

